export async function POST() {
  try {
    const members: { score: number; member: string }[] = WALLETS.map((wallet) => ({
      score: Math.floor(800 + Math.random() * 14200),
      member: wallet,
    }));

    members.sort((a, b) => b.score - a.score);

    await redis.del(LEADERBOARD_KEY);
    for (const m of members) {
      await redis.zadd(LEADERBOARD_KEY, { score: m.score, member: m.member });
    }

    return NextResponse.json({
      seeded: members.length,
      top3: members.slice(0, 3).map((m) => ({ player: m.member, score: m.score })),
    });
  } catch (e: any) {
    return NextResponse.json({ error: e.message }, { status: 500 });
  }
}
